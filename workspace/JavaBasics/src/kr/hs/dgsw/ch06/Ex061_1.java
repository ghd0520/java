package kr.hs.dgsw.ch06;

public class Ex061_1 {

	public static void main(String[] args) {
		
		pmmd(3,2);

	}

	static void pmmd(int m, int n){
		
		System.out.println(m + " + " + n + " = " + (m+n));
		System.out.println(m + " - " + n + " = " + (m-n));
		System.out.println(m + " * " + n + " = " + (m*n));
		System.out.println(m + " / " + n + " = " + (m/n));
		System.out.println(m + " % " + n + " = " + (m%n));
		
	}
}
